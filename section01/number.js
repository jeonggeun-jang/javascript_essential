'use strict';

/**
 * JavaScript 정수 / 실수
 * - 자바스크립트는 정수와 실수를 구분하지 않음
 * - 1, 1.0, 1. 모두 '1.0'으로 간주
 * - 단, 표시는 '1'로 표시
 *
 * JavaScript - IEEE 754 Standard
 * - 자바스크립트는 IEEE 754 표준을 준수 / 64비트 부동 소수점 처리
 * - ES6에 정수, 실수 구분 추가
 */

const num1 = 0.12;
const num2 = 5;

console.log(`num1 + num2 = ${num1 + num2}`);
